function PawnShopMenu()
	local player = PlayerPedId()
	if Config.FreezearPosicion then
		FreezeEntityPosition(player,true)
	end
	
	local elements = {
		{ label = '<span style="color:green;">Comprar', action = "Comprar_Menu" },
		{ label = '<span style="color:orangered;">Vender', action = "Vender_Menu" },
	}
	
	if Config.Salir then
		table.insert(elements, {label = '<span style="color:yellow;">🔚 Salir<span style="color:white;">', action = "salir",})
	end
	
	ESX.UI.Menu.Open('default', GetCurrentResourceName(), "strepsils_pawnshop_principal_menu",
		{
			title    = '<span style="color:yellow;">💶 Tienda de Segunda Mano',
			align    = Config.Alineacion,
			elements = elements
		},
	function(data, menu)
		local action = data.current.action

		if action == "Comprar_Menu" then
			PawnShopBuyMenu()
		elseif action == "Vender_Menu" then
			PawnShopSellMenu()
		elseif action == "salir" then
			CerrarMenu()
		end	
	end, function(data, menu)
		menu.close()
		insideMarker = false
		if Config.FreezearPosicion then
			FreezeEntityPosition(player,false)
		end
	end, function(data, menu)
	end)
end

