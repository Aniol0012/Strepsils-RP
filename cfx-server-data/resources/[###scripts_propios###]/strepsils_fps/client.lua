Citizen.CreateThread(function()
	while not NetworkIsPlayerActive(PlayerId()) or not NetworkIsSessionStarted() do
        Citizen.Wait(0)
        prevframes = GetFrameCount()
        prevtime = GetGameTimer()
    end

    while true do
        Citizen.Wait(1)
        curtime = GetGameTimer()
        curframes = GetFrameCount()

        if (curtime - prevtime) > 1000 then
            fps = (curframes - prevframes) - 1              
            prevtime = curtime
            prevframes = curframes
        end

		if fpsVisibles then
			--HideHudComponentThisFrame(20)
		    if fps > 0 and fps < 12 then
		        displayText("~s~FPS: ~r~" .. fps, 0, 255, 255, 255, 255, 0.97, 0.0)
		    elseif fps > 0 and fps < 30 then
		        displayText("~s~FPS: ~o~" .. fps, 0, 255, 255, 255, 255, 0.97, 0.0)
		    elseif fps > 0 and fps < 1000 then
		        displayText("~s~FPS: ~g~" .. fps, 0, 255, 255, 255, 255, 0.97, 0.0)
		    end
		end
		
		if not fpsVisibles then
			--ShowHudComponentThisFrame(20)
		end
    end
end)

