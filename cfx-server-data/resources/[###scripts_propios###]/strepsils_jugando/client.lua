Citizen.CreateThread(function()
	while true do			
		
		if playerLoaded then	-- Revisando si el jugador ha cargado
			if ESX.PlayerData.job then
					SetDiscordRichPresenceAssetSmall(ESX.PlayerData.job.name) -- Poniendo la imagen de los trabajos de los jugadores
					job = ESX.PlayerData.job.label
					jobGrade = ESX.PlayerData.job.grade_label

					SetDiscordRichPresenceAssetSmallText(job .. " - " .. jobGrade)	-- El texto del trabajo
				else			
				Citizen.Wait(500)
			end
		end					
			
		SetDiscordAppId(Config.ClientID)
		
		--SetDiscordAppId(741280443961442384) -- Discord API (Client ID) [https://discord.com/developers/applications]

		SetDiscordRichPresenceAsset('strepsils_rp')  -- Imagen grande
		
		
		
		if Config.UseESXIdentity and playerLoaded then			
			TriggerServerEvent(('discord:server:setName'))
		else
			SetRichPresence('ID:' .. GetPlayerServerId(NetworkGetEntityOwner(GetPlayerPed(-1))) .. ' | ' .. GetPlayerName(PlayerId()) .. ' | ' ..' '.. Config.PlayerText ..' ' .. #GetActivePlayers() .. '/' .. tostring(Config.PlayerCount))
        end

		----SetRichPresence('https://discord.gg/VjQ9trk')
		
        SetDiscordRichPresenceAssetText('Strepsils RP')  -- Texto h1
	
		Citizen.Wait(Config.ResourceTimer*1000)
		--Citizen.Wait(60000) -- Se actualiza cada minuto
	end
end)

