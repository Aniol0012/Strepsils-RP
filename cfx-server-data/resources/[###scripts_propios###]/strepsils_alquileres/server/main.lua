ESX.RegisterServerCallback("strepsils_chequeardinero_alquilerveh", function(source, cb)
    local src = source
    local xPlayer = ESX.GetPlayerFromId(src)
    local money = xPlayer.getMoney()

    cb(money)
end)


RegisterServerEvent("strepsils_quitardinero_alquilerveh")
AddEventHandler("strepsils_quitardinero_alquilerveh", function(money)
    local src = source
    local xPlayer = ESX.GetPlayerFromId(src)

    xPlayer.removeMoney(money)
end)

