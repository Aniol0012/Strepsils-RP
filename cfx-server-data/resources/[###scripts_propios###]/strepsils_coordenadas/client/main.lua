function DrawGenericText(text)
	SetTextColour(255, 255, 255, 255)
	SetTextFont(7)
	SetTextScale(0.478, 0.478)
	SetTextWrap(0.0, 1.0)
	SetTextCentre(false)
	SetTextDropshadow(255, 0, 0, 0, 255)
	SetTextEdge(1, 255, 0, 0, 205)
	SetTextEntry("STRING")
	AddTextComponentString(text)
	DrawText(0.40, 0.00)
end

