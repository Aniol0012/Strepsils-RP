Citizen.CreateThread(function()
     while true do
          Citizen.Wait(1)
          if IsControlJustPressed(1, Config.Contestar) and AlertaEnCurso then
               if PlayerData.job ~= nil and PlayerData.job.name == 'police' then
					quitarblip(gx, gy, gz)
					TriggerServerEvent('strepsils_chequearcontestardisparos')
                    AlertaContestada = true
                    TriggerEvent('strepsils_crearblipdisparos', coordsX, coordsY, coordsZ)
                    AlertaEnCurso = false
               end
          elseif IsControlJustPressed(1, Config.Rechazar) and AlertaEnCurso then
               AlertaContestada = false
               AlertaEnCurso = false
			   ESX.ShowAdvancedNotification('CNP', '~r~Has colgado la llamada', '', Config.FotoMsg, 1)
          end
     end
end)

function quitarblip(gx, gy, gz)
	blipId = AddBlipForCoord(gx, gy, gz)
	RemoveBlip(blipId)
end
