RegisterServerEvent('strepsils_chequeartrabajodisparosmensaje')
AddEventHandler('strepsils_chequeartrabajodisparosmensaje', function(gx, gy, gz)
	local _source = source
	local xPlayer = ESX.GetPlayerFromId(_source)
	local xPlayers = ESX.GetPlayers()

	for i = 1, #xPlayers, 1 do
		local thePlayer = ESX.GetPlayerFromId(xPlayers[i])
		if thePlayer.job.name == 'police' then
			TriggerClientEvent('strepsils_mensajepoliciaalertadisparos', xPlayers[i], gx, gy, gz)
		end
	end
end)


RegisterServerEvent('strepsils_chequearcontestardisparos')
AddEventHandler('strepsils_chequearcontestardisparos', function(gx, gy, gz)
	local _source = source
	local xPlayer = ESX.GetPlayerFromId(_source)
	local name = xPlayer.getName(source)
	local xPlayers = ESX.GetPlayers()

	for i = 1, #xPlayers, 1 do
		local thePlayer = ESX.GetPlayerFromId(xPlayers[i])
		if thePlayer.job.name == 'police' then
			TriggerClientEvent('strepsils_contestardisparos', xPlayers[i], name)
		end
	end
end)
